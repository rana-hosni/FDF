#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *) s;
	while (n > 0)
	{
		if ((unsigned char) c == *p)
			return (p);
		n--;
		p++;
	}
	return (0);
}
